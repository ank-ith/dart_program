void main(){
  String size='l';
  if(size=='xs'){
    print('shirt size is xs');
  }
  else if(size=='s'){
    print('shirt size is s');
  }
  else if(size=='m'){
    print('shirt size is m');
  }
  else if(size=='l'){
    print('shirt size is l');
  }
  else if(size=='xl'){
    print('shirt size is xl');
  }
  else if(size=='xxl'){
    print('shirt size is xxl');
  }
  else{
    print('size not available');
  }
}